#ifndef KERNEL_ARCH_X86_ARCH_LAYOUT_H
#define KERNEL_ARCH_X86_ARCH_LAYOUT_H

#include <stdint.h>

#define ARCH_HEAP_START 0x00052000u
#define ARCH_HEAP_END 0x0009F000u

#define ARCH_KERNEL_DIRECT_MAP_END ((uintptr_t)0x10000000u)
#define ARCH_KERNEL_VIRT_BASE ((uintptr_t)0xC0000000u)
#define ARCH_KERNEL_DIRECT_PHYS_MAX ((uintptr_t)0x40000000u)
#define ARCH_KERNEL_PHYS_TO_VIRT(p) ((uintptr_t)(p) + ARCH_KERNEL_VIRT_BASE)
#define ARCH_KERNEL_VIRT_TO_PHYS(v) ((uintptr_t)(v) - ARCH_KERNEL_VIRT_BASE)
#define ARCH_USER_VADDR_MIN ((uintptr_t)0x00010000u)
#define ARCH_USER_VADDR_MAX ((uintptr_t)0xC0000000u)
#define ARCH_USER_IMAGE_BASE ((uintptr_t)0x08048000u)
#define ARCH_USER_STACK_TOP ((uintptr_t)0xC0000000u)
#define ARCH_USER_STACK_BASE (ARCH_USER_STACK_TOP - 64u * 0x1000u)
#define ARCH_USER_HEAP_MAX ARCH_USER_STACK_BASE
#define ARCH_USER_MMAP_MIN 0x40000000u

#endif
